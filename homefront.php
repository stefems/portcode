					<h1 style="color: white;">Homefront is</h1>
					<h3 style="color: grey;">a 2D, story and turn-based game that was created utilizing
					primary and secondary sources involving the Vietnam War. While Stefan was the only person who developed
					the game, it was his 3 team members who developed the story, music, and art assets. The team considered using a 
					digital drawing pad for the art, but ended up taking high resolution images of pencil-drawn
					sketches, giving a unique aesthetic. The historical documents found gave a 
					surplus of anecdotal information from the American families that had sons or fathers die in the war,
					and so the objective with <i>Homefront</i> was to create a game that used real stories to
					give life to the story created for the game. The player controls a child who has been told by his mother that his
					father, a soldier in Vietnam, has died. The player controls the child and can
					walk around his home, speak to his mother, interact with items like a radio, a portrait of his father, and his father's guitar.
					In each interaction historical sources are used to give authenticity to the environment. As the player explorers the home
					the child has imagined fights with "monsters" that remind him of his father and the war that took him away.
					</h3>
