					<h3 style="color: grey;">a 2D, story and turn-based fighting game that was created utilizing
					primary and secondary sources involving the Vietnam War. While I was the only person who developed
					the game, it was my 3 team members who developed the story, music, and art assets. We considered using a 
					digital drawing pad for the art, but we ended up taking high resolution images of pencil-drawn
					sketches giving us a wholly unique aesthetic. The historical documents we found gave us a 
					surplus of anecdotal information from the American families that had sons or fathers die in the war,
					and so our objective with <i>Homefront</i> was to create a game that used real stories to
					give life to our constructed story of a son greiving the death of his father. The player controls the child and can
					walk around his home, speak to his mother, interact with items like a radio, a portrait of his father, and his father's guitar.
					In each interaction historical sources are used to give authenticity to the environment. As the player explorers the home
					the child has with imagined fights with "monsters" that remind him of his father and the war that took him away.
