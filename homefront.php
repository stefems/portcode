					<h3 style="color: white">Lessons Learned</h3>
					<h3 style="color: white"><ul>
						<li>Research yields inspiration.</li>
						<li>Details make a world of difference.</li>
						<li>Team brainstorming.</li>
					</ul></h3>
					
					<h3><a class="smallertitle">When I studied abroad in London of 2014 I discoverd a love for research. I
					had already done research for other classes, but I found something special in the experience of entering the
					British Library and combing through old documents written by people from a different place and time.
					I would often get distracted as I read through the texts, thinking about how the contents would make for
					interesting elements in video games. When I began <i>Homefront</i> I realized that historical documents could be
					used extensively across the game, and as we did more and more research we became more and more inspired. While
					extensive historical research might not be relevant for all games, I have plans to create games that borrow
					heavily from history, religion, myth, and literature.
					</a></h3>
					
					<h3 style="color: grey;"><i>Homefront</i>, (not bearing any inspiration from the action shooter of the
					same name), was inspired by games like <a href="http://www.gonehomegame.com/"><i>Gone Home</i></a>, <a href="https://en.wikipedia.org/wiki/EarthBound"><i>EarthBound</i></a> (<i>Mother</i> in Japan)
					 and <a href="http://www.krillbite.com/ats/"><i>Among the Sleep</i></a>. We wanted battles with monsters and
					complex family relationships like those seen in <i>EarthBound</i>, we wanted to convey a sense of innocence and fear 
					 like that in <i>Among the Sleep</i>, and we wanted to have an environment rich with detail and story like that
					 in <i>Gone Home</i>. In the games I've played, I've found that it's the detail that makes the experience great. Detail
					 rewards the patient players, it can give a sense of scale and depth, and it can simply make things <i>feel</i> better.
					 Developing this game has taught me that you can have detail, and that while not every player may see or appreciate the
					 detail put into the game, putting in the extra effort can make a big difference for how the game is perceived.</h3>
					
					<h3 style="color: white">
					 A finer detail not mentioned in the previous notes is that the game actually begins in a fantasy
					setting. The player doesn't control a child in a suburban American home: the child is a prince,
					his mother a benevolent fortune teller, and his father a legendary knight away at war with the
					goblin menace. As the player explorers more of the "castle" the fantastical details and imagery break
					down and reveal the truth underneath: they're just a family in a small home. We wanted the deterioration
					of fantasy to reality to demonstrate the innocence the child lost as he became more and more exposed to the
					events surrounding the Vietnam War. This idea was born in a brainstorming session and grew into our proudest
					element of the game. Our initial brainstorming sessions were phenomenal: our team really clicked and
					connected over the project. Many of my prior projects were solo-ventures. If I learned anything from
					this experience, it's that teams can develop incredible things.
					</h3>
					
