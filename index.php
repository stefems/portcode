				<p class="writingtext">	To establish a conclusion today, 40 years following its outcome, about the Vietnam War requires
				a dizzying effort of fact-checking, double-fact- checking and source-scrounging. If peering into the
				historiography of Vietnam is itself a dizzying ordeal, is it no coincidence that the American perception of
				Vietnam is also perplexed? In order to demonstrate the confusion inherent in the historiography of
				Vietnam, a look into several documents and their approaches of presenting information is necessary,
				and in combination with a criticism and contextualization of them and the facts they present, it will
				become apparent that the documentation and the facts of the Vietnam War were often misleading,
				frequently counter-intuitive, and ultimately confusing for the American audience.</p>
