				<p id="historytext" class="hiddentext">Before the appropriate analysis, it is emphasized that the scope of this essay is clearly not such
				that it will survey a vast number of Vietnam documents in order to prove its point. Many who have
				researched and written on Vietnam have worked toward achieving this goal, and the purpose of this
				essay is to contribute to the larger discussion, even if it be with a small voice. The large voices, like that
				of Wesley Fishel, editor of Vietnam: Anatomy of a Conflict published in 1968, have come to a consensus
				that the data surrounding Vietnam is confusing and often contradictory. In the preface he discusses the
				media’s role in presenting information: “Embellishments have been mixed with rumors, half-truths,
				outright fabrications, dubious statistics, and a few substantiable facts to form fantastic mélanges
				accepted by the gullible – and there are many – as accurate representations of reality.” 1 Fishel goes on
				to criticize the lack of academic study: “The absence of substantial institutional commitments to
				research or instruction on Vietnam is paradoxical given the saturation of the American (and world) mass
				media with information and opinions about it.” 2 In George Katsiaficas’s collection of sources, Vietnam
				Documents: American and Vietnamese Views of the War, published several years later in 1992, he
				describes his take on the Vietnam historiography as well: “the documents collected here illustrate the
				differing perceptions and beliefs … In selecting the contents of each chapter, I have provided a balance
				so the reader can experience the conflicting points of view.” 3 And as John Rowe argues in the Vietnam
				War and American Culture published a year earlier in 1991, “the enormous volume of conflicting
				accounts of Vietnam has given special significance to the irreducible textuality of history”, this essay will
				offer its own criticisms and analyses to arrive at the same conclusion. 4 While there are many reasons for
