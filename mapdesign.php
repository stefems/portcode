				<h3 style="color: grey" >
					Degradation is a close-quarters map developed in Halo 5's forge mode. The process of
					making the map began with me searching the template map called Ravine (hence the waterfall
					and water elements) for a location that could contain the map I would create. I loved
					the waterfall and the small pond and the surrounding cliffs so I sketched out the buildings
					and walkways on paper to get an idea where to begin. From that point I jumped into the Forge
					mode and began placing walls, rocks, floors and more to make the map. The small cave was hand-made
					with placed rocks as well. I tested the map out with 
					friends to get feedback, and modified it once more for entry in a competition. (Spoiler: I 
					didn't win.) If you're interested in a video, take a look at my <a href="http://www.forgehub.com/maps/degradation.1862/">submission page.</a>
