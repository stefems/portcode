					Riptide was made in Halo 4's Forge mode. This map was heavily inspired by maps made
					by the devolopers of previous Halo maps and I wanted to capture some of the magic
					from those maps while still adding my own interpretation. I had yet to study level design in depth
					when I made this map, and its cramped and abstruse (or the lack-thereof) layout demonstrates my inexperience.
